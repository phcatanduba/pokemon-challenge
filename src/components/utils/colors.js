export function color(type) {
  if (type === "bug") {
    return "#89960B";
  } else if (type === "water") {
    return "#5CC1ED";
  } else if (type === "fairy") {
    return "#DA93DD";
  } else if (type === "ghost") {
    return "#AD6EEC";
  } else if (type === "normal") {
    return "#C3C0B8";
  } else if (type === "ice") {
    return "#9BDEFB";
  } else if (type === "dark") {
    return "#322C26";
  } else if (type === "fighting") {
    return "#80311D";
  } else if (type === "grass") {
    return "#68BB2B";
  } else if (type === "poison") {
    return "#924694";
  } else if (type === "flying") {
    return "#5D74D5";
  } else if (type === "dragon") {
    return "#6B57D2";
  } else if (type === "fire") {
    return "#EC5D35";
  } else if (type === "ground") {
    return "#D0B155";
  } else if (type === "psychic") {
    return "#DA3063";
  } else if (type === "electric") {
    return "#F4CB38";
  } else if (type === "rock") {
    return "#9D853C";
  } else if (type === "steel") {
    return "#8F8E9E";
  }
}
